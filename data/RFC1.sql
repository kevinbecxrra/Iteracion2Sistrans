SELECT ID_CONTRATO, COSTO, CAPACIDAD, VECES
FROM CONTRATO CTS JOIN (SELECT ID_CONTRATO, VECES
		    FROM (SELECT ID_CONTRATO, COUNT(ID_CONTRATO) AS VECES
		    FROM RESERVA
	            GROUP BY ID_CONTRATO
	            ORDER BY VECES DESC)
	            WHERE ROWNUM<=20) POP
	            ON CTS.ID=POP.ID_CONTRATO;